    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //** BUSCAR PIZZA **//
        String nombre=pbn.getText();
        Pizzabase pizza = null ;
        try {
            //Llamdo metodo(nombre)
           pizza =   controlpizza.buscarPizza(Integer.parseInt(nombre));
        } catch (Exception ex) {
            Logger.getLogger(VistaAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        pbid.setText(pizza.getPizzaId().toString());
        pbp.setText(pizza.getPresentacion());
        int precio= pizza.getPrecio();
        
        pbpr.setText(""+precio);
        pbt.setText(pizza.getTamano());
        
        pfoto=pizza.getFoto();
        Image  img;
        try {
        img = ConvertirImagen(pfoto);
        Icon icon = new ImageIcon( img.getScaledInstance((int)144,(int)144, Image.SCALE_AREA_AVERAGING) );            
        pbf.setText("");
        pbf.setIcon( icon  );
        }catch (IOException ex) {
            Logger.getLogger(VistaAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

